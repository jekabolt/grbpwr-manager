-- ============================================================================
-- 4) Backfill collection (OPEN dict): normalise distinct free-text into codes, then link the style.
--    Slug = upper-cased, non-alnum -> '_', trimmed of surrounding '_', capped at 64. Placeholder/empty
--    ('', 'string') values are treated as unset (collection_id left NULL — allowed).
-- ============================================================================

INSERT IGNORE INTO collection (code, name)
SELECT DISTINCT
    TRIM(BOTH '_' FROM UPPER(LEFT(REGEXP_REPLACE(TRIM(t.collection), '[^0-9A-Za-z]+', '_'), 64))) AS code,
    TRIM(t.collection) AS name
FROM tech_card t
WHERE t.collection IS NOT NULL
  AND TRIM(t.collection) <> ''
  AND LOWER(TRIM(t.collection)) <> 'string'
  AND TRIM(BOTH '_' FROM UPPER(LEFT(REGEXP_REPLACE(TRIM(t.collection), '[^0-9A-Za-z]+', '_'), 64))) <> '';

UPDATE tech_card t
JOIN collection c
  ON c.code = TRIM(BOTH '_' FROM UPPER(LEFT(REGEXP_REPLACE(TRIM(t.collection), '[^0-9A-Za-z]+', '_'), 64)))
SET t.collection_id = c.id
WHERE t.collection_id IS NULL
  AND t.collection IS NOT NULL
  AND TRIM(t.collection) <> ''
  AND LOWER(TRIM(t.collection)) <> 'string';

-- ============================================================================
-- 5) Backfill tag (OPEN dict): same normalisation over product_tag.tag.
-- ============================================================================

INSERT IGNORE INTO tag (code, name)
SELECT DISTINCT
    TRIM(BOTH '_' FROM UPPER(LEFT(REGEXP_REPLACE(TRIM(pt.tag), '[^0-9A-Za-z]+', '_'), 64))) AS code,
    TRIM(pt.tag) AS name
FROM product_tag pt
WHERE pt.tag IS NOT NULL
  AND TRIM(pt.tag) <> ''
  AND LOWER(TRIM(pt.tag)) <> 'string'
  AND TRIM(BOTH '_' FROM UPPER(LEFT(REGEXP_REPLACE(TRIM(pt.tag), '[^0-9A-Za-z]+', '_'), 64))) <> '';

UPDATE product_tag pt
JOIN tag g
  ON g.code = TRIM(BOTH '_' FROM UPPER(LEFT(REGEXP_REPLACE(TRIM(pt.tag), '[^0-9A-Za-z]+', '_'), 64)))
SET pt.tag_id = g.id
WHERE pt.tag_id IS NULL
  AND pt.tag IS NOT NULL
  AND TRIM(pt.tag) <> ''
  AND LOWER(TRIM(pt.tag)) <> 'string';

