-- +migrate Up

-- R9 merch dictionaries. Colour is already a controlled dictionary (0130); this migration extends the
-- same discipline to COLLECTION, TAG and COUNTRY, adds the shared cache-invalidation counter
-- (dictionary_revision) and the archive-not-delete column on colour.
--
-- Ownership per R9: collection -> style (tech_card.collection_id); tag, country -> colourway
-- (product_tag.tag_id, product.country_code). Country is a CLOSED ISO 3166-1 alpha-2 dictionary (seed of
-- 249 codes, admin may only toggle `active`); collection and tag are OPEN dictionaries seeded by
-- normalising the existing free-text values.
--
-- This is the ADDITIVE + BACKFILL phase of the expand/backfill/contract chain. The new *_code / *_id
-- columns are nullable and every FK is RESTRICT (archive-not-delete). The legacy free-text columns
-- (product.country_of_origin, tech_card.collection, product_tag.tag) are deliberately NOT dropped here:
-- the destructive contract step is gated on (a) a rehearsal against a prod snapshot (R10 mandates
-- rehearsal and this environment has no DB) and (b) the T-B contract that stops the write/read path from
-- depending on the free-text columns. See the tail note.
--
-- Country backfill safety: the alias table reproduces the application's own free-text normalisation
-- (entity.countryNameToISO2 + ISO2 identity + ISO3->ISO2), FILTERED to codes that exist in the seed.
-- Codes the seed does not contain (e.g. XK/Kosovo, which has no ISO2->ISO3 counterpart) are dropped from
-- the alias set, so an unmappable value cannot cause an FK violation — it lands in the quarantine report
-- and trips the fail-fast guard instead, with a clear place to look.
--
-- Idempotent: tables are created with IF NOT EXISTS guards; guarded ADD COLUMN / ADD CONSTRAINT via information_schema
-- (multi-line PREPARE/EXECUTE/DEALLOCATE — a single line trips 1064 on the managed DSN, see 0124/0130);
-- seeds via INSERT IGNORE; backfills touch only still-NULL rows; report/guard tables are rebuilt each run.

-- ============================================================================
-- 1) Dictionary tables
-- ============================================================================

CREATE TABLE IF NOT EXISTS collection (
    id           INT PRIMARY KEY AUTO_INCREMENT,
    code         VARCHAR(64)  NOT NULL,
    name         VARCHAR(255) NOT NULL,
    translations JSON         NULL DEFAULT NULL,
    archived_at  TIMESTAMP    NULL DEFAULT NULL,
    created_at   TIMESTAMP    NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at   TIMESTAMP    NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    CONSTRAINT uniq_collection_code UNIQUE (code)
);

CREATE TABLE IF NOT EXISTS tag (
    id           INT PRIMARY KEY AUTO_INCREMENT,
    code         VARCHAR(64)  NOT NULL,
    name         VARCHAR(255) NOT NULL,
    translations JSON         NULL DEFAULT NULL,
    archived_at  TIMESTAMP    NULL DEFAULT NULL,
    created_at   TIMESTAMP    NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at   TIMESTAMP    NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    CONSTRAINT uniq_tag_code UNIQUE (code)
);

CREATE TABLE IF NOT EXISTS country (
    code         CHAR(2)      NOT NULL,
    display_name VARCHAR(128) NOT NULL,
    translations JSON         NULL DEFAULT NULL,
    active       TINYINT(1)   NOT NULL DEFAULT 1,
    created_at   TIMESTAMP    NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at   TIMESTAMP    NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    PRIMARY KEY (code),
    CONSTRAINT chk_country_code_iso2_len CHECK (CHAR_LENGTH(code) = 2)
);

CREATE TABLE IF NOT EXISTS dictionary_revision (
    namespace  VARCHAR(32) NOT NULL,
    revision   BIGINT      NOT NULL DEFAULT 1,
    updated_at TIMESTAMP   NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    PRIMARY KEY (namespace)
);

