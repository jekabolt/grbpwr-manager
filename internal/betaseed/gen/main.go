// Command gen parses the admin/auth/frontend .proto files and emits thin typed
// wrapper methods on *betaseed.Client that call c.call(ctx, verb, path, in, out)
// over the JSON gateway — one method per rpc. The request/response Go types come
// straight from the rpc signature (no naming assumptions; no rpc in these
// services uses google.protobuf.Empty, verified).
//
// Regenerate from the repo root:
//
//	go run ./internal/betaseed/gen
package main

import (
	"fmt"
	"os"
	"regexp"
	"sort"
	"strings"
)

type rpc struct {
	name, req, resp, verb, path string
}

var (
	reRPC  = regexp.MustCompile(`^\s*rpc\s+(\w+)\s*\(\s*(\w+)\s*\)\s*returns\s*\(\s*(\w+)\s*\)`)
	reVerb = regexp.MustCompile(`\b(get|post|put|patch|delete)\s*:\s*"([^"]+)"`)
)

// parse extracts rpcs + their first http verb/path from a proto file.
func parse(path string) []rpc {
	b, err := os.ReadFile(path)
	if err != nil {
		panic(err)
	}
	lines := strings.Split(string(b), "\n")
	var out []rpc
	for i := 0; i < len(lines); i++ {
		m := reRPC.FindStringSubmatch(lines[i])
		if m == nil {
			continue
		}
		r := rpc{name: m[1], req: m[2], resp: m[3]}
		// scan forward (this line + up to ~12 lines) for the first verb:path.
		for j := i; j < len(lines) && j < i+14; j++ {
			if vm := reVerb.FindStringSubmatch(lines[j]); vm != nil {
				r.verb = strings.ToUpper(vm[1])
				r.path = vm[2]
				break
			}
			if j > i && reRPC.MatchString(lines[j]) {
				break // hit next rpc without an annotation
			}
		}
		if r.verb == "" {
			fmt.Fprintf(os.Stderr, "WARN: no http annotation for rpc %s (%s)\n", r.name, path)
			continue
		}
		out = append(out, r)
	}
	return out
}

func main() {
	type svc struct {
		file, alias, prefix string
	}
	svcs := []svc{
		{"proto/admin/admin/admin.proto", "admin", ""},
		{"proto/auth/auth/auth.proto", "auth", "Auth"},
		{"proto/frontend/frontend/frontend.proto", "frontend", "SF"},
	}

	var sb strings.Builder
	sb.WriteString("// Code generated by internal/betaseed/gen. DO NOT EDIT.\n")
	sb.WriteString("package betaseed\n\n")
	sb.WriteString("import (\n\t\"context\"\n\n")
	sb.WriteString("\tadmin \"github.com/jekabolt/grbpwr-manager/proto/gen/admin\"\n")
	sb.WriteString("\tauth \"github.com/jekabolt/grbpwr-manager/proto/gen/auth\"\n")
	sb.WriteString("\tfrontend \"github.com/jekabolt/grbpwr-manager/proto/gen/frontend\"\n)\n\n")
	sb.WriteString("var _ = context.Background\n\n")

	seen := map[string]bool{}
	total := 0
	for _, s := range svcs {
		rpcs := parse(s.file)
		sort.Slice(rpcs, func(a, b int) bool { return rpcs[a].name < rpcs[b].name })
		fmt.Fprintf(&sb, "// ---- %s (%d rpc) ----\n\n", s.alias, len(rpcs))
		for _, r := range rpcs {
			method := s.prefix + r.name
			if seen[method] {
				fmt.Fprintf(os.Stderr, "WARN: duplicate method %s, skipping\n", method)
				continue
			}
			seen[method] = true
			total++
			fmt.Fprintf(&sb,
				"func (c *Client) %s(ctx context.Context, in *%s.%s) (*%s.%s, error) {\n"+
					"\tout := new(%s.%s)\n"+
					"\tif err := c.call(ctx, %q, %q, in, out); err != nil {\n\t\treturn nil, err\n\t}\n"+
					"\treturn out, nil\n}\n\n",
				method, s.alias, r.req, s.alias, r.resp, s.alias, r.resp, r.verb, r.path)
		}
	}

	if err := os.WriteFile("internal/betaseed/rpc_gen.go", []byte(sb.String()), 0o644); err != nil {
		panic(err)
	}
	fmt.Printf("wrote internal/betaseed/rpc_gen.go — %d methods\n", total)
}
